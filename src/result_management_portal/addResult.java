    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = T1.getText();
        String physics = T2.getText();
        String maths = T3.getText();
        String EM = T4.getText();
        String DBMS = T5.getText();
        String OS = T6.getText();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms1","root","prathik@007");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"' ");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollno,physics,maths,EM,DBMS,OS) values('"+rollno+"','"+physics+"','"+maths+"','"+EM+"','"+DBMS+"','"+OS+"' )");
                JOptionPane.showMessageDialog(null,"Marks saved successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"This rollno is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

